package com.contents.db;

public class MovieInfoVO {

	String name;
	String play_start;
	String play_end;
	String image_file;
	int grade;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPlay_start() {
		return play_start;
	}
	public void setPlay_start(String play_start) {
		this.play_start = play_start;
	}
	public String getPlay_end() {
		return play_end;
	}
	public void setPlay_end(String play_end) {
		this.play_end = play_end;
	}
	public String getImage_file() {
		return image_file;
	}
	public void setImage_file(String image_file) {
		this.image_file = image_file;
	}
	public int getGrade() {
		return grade;
	}
	public void setGrade(int grade) {
		this.grade = grade;
	}
	
	

}
